#include "ft_printf.h"

int	ft_putnbr(int n)
{
	long	i;
	int		j;

	j = 0;
	i = n;
	if (i < 0)
	{
		i = -i;
		j += ft_putchar('-');
	}
	if (i >= 10)
	{
		j += ft_putnbr(i / 10);
	}
	j += ft_putchar((i % 10) + '0');
	return (j);
}
